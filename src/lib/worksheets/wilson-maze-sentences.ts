/**
 * Get only illustratable sentences (for Draw & Write worksheets)
 */
export function getIllustratableSentences(substep: string): MazeSentence[] {
  const sentences = WILSON_MAZE_SENTENCES[substep] || [];
  return sentences.filter(s => s.illustratable);
}

